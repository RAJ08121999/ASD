print("working on bitwise operators")

a=5
b=10

print(a|b) 
#15


# 0101---->5
# 1010---->10
# 1111---->15

print(a&b)
#0


# 0101---->5
# 1010---->10
# 0000---->0


print(a^b)
#15


# 0101---->5
# 1010---->10
# 1111---->15

print(~a)
#-6


# 0101----->5
# 1010------>-6


print(a<<2)
#20

# 5 = 00000101
# 00000101 << 2 ----> 00010100
# 00010100 = 20



print(a>>2)
#1


# 5 = 00000101
# 00000101 >> 2 ----> 00000001
# 00000001 = 1
